    private void azuriraj2ButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_azuriraj2ButtonActionPerformed
        if (!skolskaGodina3Field.getText().matches("\\d{4}/\\d{4}")) {
            KonkursniRadnikForm.pozoviOptionPane(this, "Pogrešno unijeta skolska godina. Potreban format je xxxx/yyyy.", "Greška!", JOptionPane.ERROR_MESSAGE);
            return;
        }

        int id = -1;
        try {
            id = Integer.valueOf(idPrijaveField.getText());
        } catch (Exception ex) {
            KonkursniRadnikForm.pozoviOptionPane(this, "Greška pri unosu id.", "Greška!", JOptionPane.ERROR_MESSAGE);
            return;
        }
        PrijavaNaKonkursDTO prijava = DAOFactory.getDAOFactory().getPrijavaNaKonkursDAO().prijava(id, skolskaGodina3Field.getText());
        if (prijava == null) {
            KonkursniRadnikForm.pozoviOptionPane(this, "Ne postoji takva prijava.", "Greška!", JOptionPane.ERROR_MESSAGE);
            return;
        }

        new IzmijeniPrijavuDialog(this, true, id, skolskaGodina3Field.getText()).setVisible(true);
    }//GEN-LAST:event_azuriraj2ButtonActionPerformed

