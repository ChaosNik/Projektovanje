    private void azurirajButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_azurirajButtonActionPerformed
        String skolskaGodina = skolskaGodinaField.getText();
        String jmb = JMBField.getText();

        if (!skolskaGodina.matches("\\d{4}/\\d{4}")) {
            KonkursniRadnikForm.pozoviOptionPane(this, "Greska u formatu skolske godine. Potreban format xxxx/yyyy.", "Greska!", JOptionPane.ERROR_MESSAGE);
            return;
        }

        if (jmb.length() != 13) {
            KonkursniRadnikForm.pozoviOptionPane(this, "Greska pri unosu JMB.", "Greska!", JOptionPane.ERROR_MESSAGE);
            return;
        }

        StudentDTO student = DAOFactory.getDAOFactory().getStudentDAO().student(jmb);
        if (student == null) {
            KonkursniRadnikForm.pozoviOptionPane(this, "Nepostojeci JMB.", "Greska!", JOptionPane.ERROR_MESSAGE);
            return;
        }

        PrijavaNaKonkursDTO prijava = DAOFactory.getDAOFactory().getPrijavaNaKonkursDAO().prijava(jmb, skolskaGodina);
        if (prijava == null) {
            KonkursniRadnikForm.pozoviOptionPane(this, "Ne postoji takva prijava.", "Greska!", JOptionPane.ERROR_MESSAGE);
            return;
        }
        MjestoPrebivalistaDTO mjesto = DAOFactory.getDAOFactory().getMjestoPrebivalistaDAO().mjestoPrebivalista(prijava.getBrojPoste());

        new IzmijeniPrijavuDialog(this, true, jmb, skolskaGodina).setVisible(true);
    }//GEN-LAST:event_azurirajButtonActionPerformed

