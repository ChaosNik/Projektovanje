    public PregledPrijavaSkolskeGodineDialog(java.awt.Frame parent, boolean modal, String skolskaGodina, String fakultet) {
        super(parent, modal);
        initComponents();
        PregledPrijavaSkolskeGodineDialog.skolskaGodina = skolskaGodina;
        skolskaGodinaField.setText(skolskaGodina);

        List<PrijavaNaKonkursDTO> lista = DAOFactory.getDAOFactory().getPrijavaNaKonkursDAO().prijave(skolskaGodina);
        DefaultTableModel model;
        StudentDTO student = new StudentDTO();
        MjestoPrebivalistaDTO mjesto = new MjestoPrebivalistaDTO();
        FakultetDTO faks;

        for (PrijavaNaKonkursDTO prijava : lista) {

            student = DAOFactory.getDAOFactory().getStudentDAO().student(prijava.getJMB());
            mjesto = DAOFactory.getDAOFactory().getMjestoPrebivalistaDAO().mjestoPrebivalista(prijava.getBrojPoste());
            faks = DAOFactory.getDAOFactory().getFakultetDAO().fakultet(prijava.getNazivFakulteta());

            if (faks.getNazivFakulteta().equals(fakultet)) {
                Object[] row = {prijava.getIdPrijave(), prijava.getJMB(), student.getIme(), student.getPrezime(), mjesto.getNaziv(), prijava.getNazivFakulteta(), prijava.getSkolskaGodina(), prijava.getProsjekOcjena(), prijava.getBrojObnovljenihGodina(), prijava.getTelefon(), prijava.getEmail()};

                model = (DefaultTableModel) tabelica.getModel();

                model.addRow(row);
            }
        }

        setTitle("Pregled prijava za " + fakultet + " fakultet");
    }

