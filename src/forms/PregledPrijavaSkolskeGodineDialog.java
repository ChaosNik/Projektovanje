    private void socijalniStatusButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_socijalniStatusButtonActionPerformed
        String id = socijalniStatusField.getText();
        int flag = 0;

        DefaultTableModel model = (DefaultTableModel) tabelica.getModel();

        for (int i = 0; i < tabelica.getRowCount(); i++) {
            String value = tabelica.getModel().getValueAt(i, 0).toString();
            if (id.equals(value)) {
                flag = 1;
            }
        }
        if (flag == 1) {
            SocijalniDiagram novi = new SocijalniDiagram(new KonkursniRadnikForm(), true, Integer.valueOf(id), skolskaGodinaField.getText());
            novi.setVisible(true);
        } else {
            KonkursniRadnikForm.pozoviOptionPane(this, "Ne postoji traženi id.", "Greška!", JOptionPane.ERROR_MESSAGE);
            return;
        }

    }//GEN-LAST:event_socijalniStatusButtonActionPerformed

