    private void dodajAzurirajButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_dodajAzurirajButtonActionPerformed
        int brojKreveta = 0;
        String naziv = "";
        String adresa = "";

        try {
            brojKreveta = Integer.parseInt(brojKrevetaField.getText());
        } catch (Exception e) {
            KonkursniRadnikForm.pozoviOptionPane(this, "Greška pri unosu broja kreveta.", "Greška!", JOptionPane.ERROR_MESSAGE);
            return;
        }

        if (nazivField.getText().isEmpty()) {
            KonkursniRadnikForm.pozoviOptionPane(this, "Polje naziva fakulteta ne smije biti prazno.", "Greška!", JOptionPane.ERROR_MESSAGE);
            return;
        } else {
            naziv = nazivField.getText();
        }

        if (adresaField.getText().isEmpty()) {
            KonkursniRadnikForm.pozoviOptionPane(this, "Polje adresa fakulteta ne smije biti prazno.", "Greška!", JOptionPane.ERROR_MESSAGE);
            return;
        } else {
            adresa = adresaField.getText();
        }

        FakultetDTO staro = DAOFactory.getDAOFactory().getFakultetDAO().fakultet(naziv);
        FakultetDTO novo = new FakultetDTO(naziv, adresa, brojKreveta);
        if (staro != null) {
            new PotvrdiAzuriranjeFakultetaDialog(parent, true, staro, novo).setVisible(true);
        } else {
            boolean flag = DAOFactory.getDAOFactory().getFakultetDAO().dodajFakultet(novo);

            if (flag) {
                KonkursniRadnikForm.pozoviOptionPane(this, "Uspješno dodavanje.", "", JOptionPane.INFORMATION_MESSAGE);
                return;
            } else {
                KonkursniRadnikForm.pozoviOptionPane(this, "Neuspješno dodavanje!", "Greška!", JOptionPane.ERROR_MESSAGE);;
                return;
            }
        }
    }//GEN-LAST:event_dodajAzurirajButtonActionPerformed

