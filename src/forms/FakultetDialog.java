    private void izbrisiButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_izbrisiButtonActionPerformed
        boolean flag = DAOFactory.getDAOFactory().getFakultetDAO().obrisiFakultet(nazivField2.getText());
        if (flag) {
            KonkursniRadnikForm.pozoviOptionPane(this, "Uspješno brisanje.", "", JOptionPane.INFORMATION_MESSAGE);
            return;
        } else {
            KonkursniRadnikForm.pozoviOptionPane(this, "Ne postoji takav podatak u bazi.", "Greška!", JOptionPane.ERROR_MESSAGE);
            return;
        }
    }//GEN-LAST:event_izbrisiButtonActionPerformed

