    private void kreirajButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_kreirajButtonActionPerformed
        if (usernameField.getText().equals("") || passwordField.getText().equals("") || repeatPasswordField.getText().equals("")) {
            KonkursniRadnikForm.pozoviOptionPane(this, "Morate popuni polja koja se tiƒçu korisniƒçkog imena i lozinke.", "Gre≈°ka!", JOptionPane.ERROR_MESSAGE);
            return;
        }
        if (!passwordField.getText().equals(repeatPasswordField.getText())) {
            KonkursniRadnikForm.pozoviOptionPane(this, "Lozinka i ponovljena lozinka nisu iste.", "Gre≈°ka!", JOptionPane.ERROR_MESSAGE);
            return;
        }
        if (passwordField.getText().length() < 8) {
            KonkursniRadnikForm.pozoviOptionPane(this, "Lozinka mora biti du≈æa od 8 karaktera.", "Gre≈°ka!", JOptionPane.ERROR_MESSAGE);
            return;
        }

        KonkursniRadnikDTO kr = DAOFactory.getDAOFactory().getKonkursniRadnikDAO().konkursniRadnik(usernameField.getText());
        AdminDTO a = DAOFactory.getDAOFactory().getAdminDAO().admin(usernameField.getText());
        BlagajnikDTO b = DAOFactory.getDAOFactory().getBlagajnikDAO().blagajnik(usernameField.getText());

        if (kr != null || a != null || b != null) {
            KonkursniRadnikForm.pozoviOptionPane(this, "Korisniƒçko ime je zauzeto!", "Gre≈°ka!", JOptionPane.ERROR_MESSAGE);
            return;
        }

        String username = usernameField.getText();
        String lozinka = passwordField.getText();
        String ime = imeField.getText();
        String prezime = prezimeField.getText();
        String jmb = JMBField.getText();
        String salt = randomString(9);
        String hesiranaLozinka = DAOFactory.getDAOFactory().getKonkursniRadnikDAO().get_SHA_512_SecurePassword(lozinka, salt);
        Calendar calendar = Calendar.getInstance();
        java.sql.Date datum = new java.sql.Date(calendar.getTime().getTime());

        if (konkursniButton.isSelected()) {
            KonkursniRadnikDTO radnik = new KonkursniRadnikDTO(username, hesiranaLozinka, salt, ime, prezime, jmb, datum, true);
            boolean flag = DAOFactory.getDAOFactory().getKonkursniRadnikDAO().dodajKonkursnogRadnika(radnik);
            if (flag) {
                KonkursniRadnikForm.pozoviOptionPane(this, "Uspje≈°no kreiran nalog.", "Gre≈°ka!", JOptionPane.INFORMATION_MESSAGE);
            } else {
                KonkursniRadnikForm.pozoviOptionPane(this, "Nalog nije kreiran!", "Gre≈°ka!", JOptionPane.ERROR_MESSAGE);
            }

        } else if (adminButton.isSelected()) {
            AdminDTO radnik = new AdminDTO(username, hesiranaLozinka, salt, ime, prezime, jmb, datum, true);
            boolean flag = DAOFactory.getDAOFactory().getAdminDAO().dodajAdmina(radnik);
            if (flag) {
                KonkursniRadnikForm.pozoviOptionPane(this, "Uspje≈°no kreiran nalog.", "Gre≈°ka!", JOptionPane.INFORMATION_MESSAGE);
            } else {
                KonkursniRadnikForm.pozoviOptionPane(this, "Nalog nije kreiran!", "Gre≈°ka!", JOptionPane.ERROR_MESSAGE);
            }
        } else if (blagajnikButton.isSelected()) {
            BlagajnikDTO radnik = new BlagajnikDTO(username, hesiranaLozinka, salt, ime, prezime, jmb, datum, true);
            boolean flag = DAOFactory.getDAOFactory().getBlagajnikDAO().dodajBlagajnika(radnik);
            if (flag) {
                KonkursniRadnikForm.pozoviOptionPane(this, "Uspje≈°no kreiran nalog.", "Gre≈°ka!", JOptionPane.INFORMATION_MESSAGE);
            } else {
                KonkursniRadnikForm.pozoviOptionPane(this, "Nalog nije kreiran!", "Gre≈°ka!", JOptionPane.ERROR_MESSAGE);
            }
        } else {
            KonkursniRadnikForm.pozoviOptionPane(this, "Morate odabrati vrstu naloga.", "Gre≈°ka!", JOptionPane.ERROR_MESSAGE);
            return;
        }
        this.dispose();
        new AdminForm().setVisible(true);
    }//GEN-LAST:event_kreirajButtonActionPerformed

