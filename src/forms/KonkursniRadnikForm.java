    private void procitajTekstKonkursaButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_procitajTekstKonkursaButtonActionPerformed
        String skolskaGodina = skolskaGodinaField.getText();

        if (!skolskaGodina.matches("\\d{4}/\\d{4}")) {
            pozoviOptionPane(this, "Greška u formatu školske godine. Potreban format xxxx/yyyy.", "Greška!", JOptionPane.ERROR_MESSAGE);
            return;
        }

        String putanja = DAOFactory.getDAOFactory().getKonkursDAO().readPDF(skolskaGodina);

        if (putanja.equals("")) {
            pozoviOptionPane(this, "Ne postoji tekst konkursa za tu godinu.", "Greška!", JOptionPane.ERROR_MESSAGE);
            return;
        }

        if (Desktop.isDesktopSupported()) {
            try {
                File myFile = new File(putanja);
                Desktop.getDesktop().open(myFile);
            } catch (IOException ex) {
                pozoviOptionPane(this, "Ne postoji aplikacija za otvaranje ovog tipa dokumenta.", "Greška!", JOptionPane.ERROR_MESSAGE);
                return;
            }
        }
    }//GEN-LAST:event_procitajTekstKonkursaButtonActionPerformed

