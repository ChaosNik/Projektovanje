    private void generisiRezultateButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_generisiRezultateButtonActionPerformed

        if (!skolskaGodinaField2.getText().matches("\\d{4}/\\d{4}")) {
            pozoviOptionPane(this, "Greška u formatu školske godine. Potreban format xxxx/yyyy.", "Greška!", JOptionPane.ERROR_MESSAGE);
            return;
        }

        List<FakultetDTO> fakulteti = DAOFactory.getDAOFactory().getFakultetDAO().fakulteti();
        List<PrijavaNaKonkursDTO> prijave = DAOFactory.getDAOFactory().getPrijavaNaKonkursDAO().prijave(skolskaGodinaField2.getText());
        List<RezultatiDTO> rezultati = DAOFactory.getDAOFactory().getRezultatiDAO().rezultati(skolskaGodinaField2.getText());

        for (PrijavaNaKonkursDTO prijava : prijave) {
            DAOFactory.getDAOFactory().getPrijavaNaKonkursDAO().bodujPrijavu(skolskaGodinaField2.getText(), prijava.getJMB());
        }

        int brojKreveta = 0;

        for (FakultetDTO faks : fakulteti) {
            brojKreveta = faks.getBrojDodjeljenihKreveta();
            for (PrijavaNaKonkursDTO pri : prijave) {
                if (pri.getNazivFakulteta().equals(faks.getNazivFakulteta())) {
                    for (RezultatiDTO rez : rezultati) {
                        if (pri.getIdPrijave() == rez.getIdPrijave()) {

                            if (brojKreveta > 0) {
                                rez.setProsaoNaKonkursu(true);
                                brojKreveta--;
                                DAOFactory.getDAOFactory().getRezultatiDAO().azurirajRezultat(rez);
                            } else {
                                rez.setProsaoNaKonkursu(false);
                                DAOFactory.getDAOFactory().getRezultatiDAO().azurirajRezultat(rez);
                            }

                        }
                    }
                }
            }
        }

        pozoviOptionPane(this, "Rezultati su generisani.", "", JOptionPane.INFORMATION_MESSAGE);
    }//GEN-LAST:event_generisiRezultateButtonActionPerformed

